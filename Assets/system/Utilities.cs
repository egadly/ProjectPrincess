using UnityEngine;
using System.Collections;

public class Utilities {

	public static Vector2 Vec2( float x, float y ) {
		Vector2 temp;
		temp.x = x;
		temp.y = y;
		return temp;
	}

	public static Vector3 Vec3( float x, float y, float z ) {
		Vector3 temp;
		temp.x = x;
		temp.y = y;
		temp.z = z;
		return temp;
	}

}
